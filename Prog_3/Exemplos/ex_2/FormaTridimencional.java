public class FormaTridimencional extends Forma {
    public FormaTridimencional(double raio, double lado) {
        super(raio, lado);
    }

    @Override
    public double getRaio() {
        return super.getRaio();
    }

    @Override
    public void setRaio(double raio) {
        super.setRaio(raio);
    }

    @Override
    public double getLado() {
        return super.getLado();
    }

    @Override
    public void setLado(double lado) {
        super.setLado(lado);
    }
}
